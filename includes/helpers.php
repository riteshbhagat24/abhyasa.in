/** Render a list of feature check items. */
function render_features(array $items, string $listClass = 'feature-list'): string {
    $out = '<ul class="'.attr($listClass).'">';
    foreach ($items as $item) {
        $out .= '<li>'.icon('check', 18).'<span>'.e($item).'</span></li>';
    }
    $out .= '</ul>';
    return $out;
}

/** Asset path helper (for easy CDN migration). */
function asset(string $path): string {
    return 'assets/' . ltrim($path, '/');
}

/** Link active-state helper. */
function is_current(string $slug): bool {
    return current_page_slug() === $slug;
}

/** Find blog post by slug. */
function find_post(array $posts, string $slug): ?array {
    foreach ($posts as $p) if ($p['slug'] === $slug) return $p;
    return null;
}

/** Basic phone/email formatting. */
function tel_link(string $num): string {
    return 'tel:' . preg_replace('/[^+\d]/', '', $num);
}

/** Safe JSON encode for data-attributes. */
function json_attr($data): string {
    return e(json_encode($data, JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES));
}
