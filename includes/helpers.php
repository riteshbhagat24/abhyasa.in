/** Escape for HTML context. */
function e(?string $s): string {
    return htmlspecialchars($s ?? '', ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8');
}

/** Escape for attribute context (same as e() but named for clarity). */
function attr(?string $s): string {
    return e($s);
}

